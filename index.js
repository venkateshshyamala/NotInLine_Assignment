let flag1=1;
function para1()
{
    if(flag1==1)
    {
        flag1=0;
        document.getElementById("dd1").style.borderRadius="40px";
        document.getElementById("btn1Icon1").style.display="none";
        document.getElementById("btn1Icon2").style.display="block";
        document.getElementById("res1").style.display="block";

    }
    else
    {
        flag1=1;
        document.getElementById("dd1").style.borderRadius="100px";
        document.getElementById("btn1Icon2").style.display="none";
        document.getElementById("btn1Icon1").style.display="block";
        document.getElementById("res1").style.display="none";

    }
}

